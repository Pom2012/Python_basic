"""
        Scanner funcation in Java
        We can ask the user for input by using the input() method
"""

name = input("Enter you name:\n")
age = int(input("Enter you age:\n"))
gpa = float(input("Enter you gpa:\n"))
boolean_value = bool(input("Enter True or False:\n"))

print(name)
print(type(name))
print(age)
print(type(age))
print(gpa)
print(type(gpa))

print(boolean_value)
print(type(boolean_value))